                        Gần đây tôi có đọc một bài báo về Marissa Mayer, CEO của Yahoo nói về việc cô ấy làm việc 130h/tuần. Mayer từng được ca ngợi là người phụ nữ làm việc chăm chỉ nhất Silicon Valley. Điều này thật tuyệt nhưng liệu có lúc nào cô ấy cảm thấy kiệt sức và giảm hiệu quả làm việc.
                        Một doanh nhân thành công vẫn có thể làm việc với số giờ thông thường mà vẫn tận hưởng cuộc sống cá nhân và trải nghiệm những thứ thực sự quan trọng.Muốn tăng năng suất thì giảm giờ làm việc đi
                        Đây là một vài điều giúp tôi cải thiện năng suất khi cắt giảm giờ làm việc:
                        - Xác định mục tiêu hàng ngày trước khi bắt đầu ngày mới: Tôi thường kết thúc mỗi ngày bằng việc viết ra mục tiêu và danh sách việc cần làm ngày kế tiếp. Bằng cách này, vào ngày hôm sau tôi có thể làm việc luôn chứ không phải gãi đầu gãi tai xem mình bắt đầu từ đâu. Nếu là doanh nhân chắc chắn bạn luôn có danh sách bất tận những thứ cần hoàn thành, cách này sẽ giúp bạn tập trung và làm việc hiệu quả hơn.
                        - Nghỉ ngơi đầy đủ: Tôi làm việc như điên khiến tôi thường xuyên mệt mỏi. Tôi uống cà phê cả ngày để tỉnh táo làm việc suốt 20h. Tôi cảm thấy quá mệt mỏi nên hiệu suất làm việc cũng khá thấp, ảnh hưởng đến công việc kinh doanh. Hãy ngủ đủ giấc mỗi đêm và dành cuối tuần để cơ thể và tâm trí phục hồi.
                        - Tập luyện hàng ngày: Bạn sẽ cảm giác tràn đầy năng lượng nếu luyện tập hàng ngày. Hãy đừng bao giờ quên lợi ích của việc có sức khỏe tốt.
                        - Dậy sớm vào buổi sáng: Thời gian trước 9h sáng bạn có thể hoàn thành nhiều việc mà không bị ai quấy nhiễu. Có nhiều người nói “Tôi chẳng phải con người buổi sáng” nhưng đó chỉ là cái cớ. Nếu bạn muốn công việc tốt lên, thực sự bạn muốn thế thì sẽ chẳng ngại ngần việc gì kể cả thức dậy sớm.
                        - Học cách ủy quyền: Đây là một điểm yếu của tôi khiến tôi phải làm việc nhiều giờ liền: Tôi không giao việc cho người khác. Tôi từng nghĩ rằng mình là người duy nhất có khả năng hoàn thành công việc, đó là việc của chính tôi. Sau này thì tôi học được cách tin tưởng nhiều hơn, giao đi những công việc nhỏ hơn, cho phép tôi làm những công việc quan trọng hiệu quả hơn.
